package Entidade;

public class Unidade {
	
		int idUnidade;
		String nome;
		
		
		public int getIdUnidade() {
			return idUnidade;
		}
		public void setIdUnidade(int idUnidade) {
			this.idUnidade = idUnidade;
		}
		public String getNome() {
			return nome;
		}
		public void setNome(String nome) {
			this.nome = nome;
		}
		
		
}
